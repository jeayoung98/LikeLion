package com.example.day02.value;

public class ValueEx {
    public static void main(String[] args) {
        // 변수 선언과 초기화 연습
        // 자바에서 정수형 변수 a,b,c를 선언하고 각각 10,20,30으로 초기화한 후, 이름의 합을 계산하여 출력
        int a; // 정수형 변수 a 선언
        int b; // 정수형 변수 b 선언
        int c; // 정수형 변수 c 선언
        a = 10; // a 초기화
        b = 20; // b 초기화
        c = 30; // c 초기화

        System.out.println("a + b + c = " + (a + b + c)); // a + b + c 출력

    }
}
