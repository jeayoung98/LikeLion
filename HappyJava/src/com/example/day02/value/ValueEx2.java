package com.example.day02.value;

public class ValueEx2 {
    public static void main(String[] args) {
        // 출력문 연습
        // 정수형 변수 x를 선언하고 어떤 값으로 초기화 한 후 , x의 값과 x값의 두배를 출력하는 프로그램을 작성하시오
        int x; // 정수형 변수 x 선언
        x = 20; // x 값 초기화
        System.out.println("x = " + x); // x값 출력
        System.out.println("x * 2 = " + x*2); // x 2배값 출력
    }
}
