package com.example.day02;

public class Book1 {
    private String title;
    private int price;

    public void setTitle(String title) {
        this.title = title;
    }
    public String getTitle() {
        return this.title;
    }

    public int getPrice() {
        return price; // Alt + int 자동으로 만듬
    }

    public void setPrice(int price) {
        this.price = price; // Alt + int 자동으로 만듬
    }
}