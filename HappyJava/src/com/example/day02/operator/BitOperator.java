package com.example.day02.operator;

public class BitOperator {
    public static void main(String[] args) {
        int a = 4;
        int b = a >> 1;
        System.out.println(b);

        int c = 4;
        int d = c << 1;
        System.out.println(d);

        int e = 8;
        int f = d << 1;
        System.out.println(f);
    }
}
