package com.example.day02.operator;

public class OpEx2 {
    public static void main(String[] args) {
        int a = 5;
        ++a;

        System.out.println(++a); //7
        System.out.println(a++); //7
        System.out.println(a);   //8
        System.out.println(Integer.MIN_VALUE);
    }
}
